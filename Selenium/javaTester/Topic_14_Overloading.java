package javaTester;

import java.util.Scanner;

public class Topic_14_Overloading {
    // Coi chừng nham với overiding nhé. Một bên là ghi đè lớp cha, một bên là nạp chồng phương thức
    //Overloading: Cùng ten nhưng khác tham số truyền vào
    private float banKinh;

    public Topic_14_Overloading() {

    }

    public Topic_14_Overloading(float banKinh) {
        this.banKinh = banKinh;
    }

    // Overloading có thể gặp ở các trường hợp nào
    public static void main(String[] args) {
        System.out.println(); // Có rất nhiều phương thức print được nạp chồng, cái này tùy thuộc sẽ
    }



}
